#include <iostream>
using namespace std;

int main ()
{
    int  a;
    a=2;
    cout<<"size of integer "<<sizeof(a)<<endl;

    float  b;
    b=2;
    cout<<"size of integer "<<sizeof(b)<<endl;

    char  c;
    cout<<"size of integer "<<sizeof(c)<<endl;

    bool  d;
    cout<<"size of integer "<<sizeof(d)<<endl;

    short int si;
    cout<<"size of integer "<<sizeof(si)<<endl;

    long int  li;
    cout<<"size of integer "<<sizeof(li)<<endl;
    return 0;
}
